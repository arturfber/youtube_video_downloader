async function fetchFile(input) {

    try{
        url = `http://video.infusiblecoder.com/video_info.php?url=${input}`
        query = await fetch(url)
        response = await query.json()
        links = response['links'][0]


        wrapper.style.display = 'none'
        results.style.display = 'flex'

        // Iframe
        iframe_src = input.replace(/.*\//, '')
        results.querySelector("iframe").src = `https://www.youtube.com/embed/${iframe_src}`
        results.querySelector("h1").innerText = response.title

        links.forEach(link => {

            if(link.mimeType.includes("video")){
                format_icon = link.audioQuality == null ? '<i class="fa-solid fa-volume-xmark"></i>' : '<i class="fa-solid fa-video"></i>'
            } else {
                format_icon = '<i class="fa-solid fa-volume-high"></i>'
            }
           
            format = link.mimeType.replace(/; codecs.*$/, '')
            quality = link.qualityLabel != null ? link.qualityLabel : link.quality

            document.querySelector(".links .content").insertAdjacentHTML('beforeend', `
                <div class="col-3">${format_icon}${format}</div>
                <div class="col-3">${quality}</div>
                <div class="col-3">${formatBytes(link.contentLength)}</div>
                <div class="col-3">
                    <a href="${link.url}" target="_blank">
                        <i class="fa-solid fa-cloud-arrow-down"></i> Baixar
                    </a>
                </div>
            `)
        });


    } catch (error) {
        alert("Erro:" + console.error)
        console.log(error);
    } 

  
}